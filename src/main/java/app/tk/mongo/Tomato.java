package app.tk.mongo;

public class Tomato {
    private float meter;
    private String image;
    private float rating;
    private float reviews;
    private float fresh;
    private String consensus;
    private float userMeter;
    private float userRating;
    private float userReviews;


    // Getter Methods

    public float getMeter() {
        return meter;
    }

    public void setMeter(float meter) {
        this.meter = meter;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public float getRating() {
        return rating;
    }

    public void setRating(float rating) {
        this.rating = rating;
    }

    public float getReviews() {
        return reviews;
    }

    public void setReviews(float reviews) {
        this.reviews = reviews;
    }

    public float getFresh() {
        return fresh;
    }

    // Setter Methods

    public void setFresh(float fresh) {
        this.fresh = fresh;
    }

    public String getConsensus() {
        return consensus;
    }

    public void setConsensus(String consensus) {
        this.consensus = consensus;
    }

    public float getUserMeter() {
        return userMeter;
    }

    public void setUserMeter(float userMeter) {
        this.userMeter = userMeter;
    }

    public float getUserRating() {
        return userRating;
    }

    public void setUserRating(float userRating) {
        this.userRating = userRating;
    }

    public float getUserReviews() {
        return userReviews;
    }

    public void setUserReviews(float userReviews) {
        this.userReviews = userReviews;
    }
}
